    <div class="media-body">
     <p align="justify" style="margin-left: 20px; margin-right: 50px; text-indent: 0.5in">Balai Penelitian Tanaman Jeruk dan Buah Subtropika (Balitjestro) pada awalnya merupakan balai penelitian milik swasta Belanda pada tahun 1930-1940. Komoditas yang diusahakan pada waktu itu adalah kopi dan buah-buahan. Dan saat ini kebun percobaan yang ada mengelola berbagai macam buah subtropika. Salah satu yang menjadi fokus utamanya adalah buah jeruk yang kini memiliki 242 varietas jeruk yang dihasilkan dari penelitian Balitjestro.</p>
      <p align="justify" style="margin-left: 20px; margin-right: 50px; text-indent: 0.5in">Setiap tahun Balai ini membuka wisata edukasi petik jeruk yang dapat dinikmati oleh pengunjung pada saat musim panen. Lokasi kebun yang dibuka untuk wisata petik tidak selalu tetap, namun menyesuaikan kebun mana saja yang saat ini sudah siap panen. Panen raya setiap tahun akan berlangsung pada bulan Juni sampai dengan Agustus. Para pengunjung yang datang dapat memetik langsung buah dari pohonnya dan menikmatinya, setiap orang yang datang mendapat kesempatan makan buah jeruk yang mereka petik sepuasnya saat dikebun sambil bersantai serta membawa pulang 2 kg buah jeruk.</p>
